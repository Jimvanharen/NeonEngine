package org.virusparadox.math;

public class Vector3 {
	
	public float x, y, z; 
	
	
	public Vector3(float x, float y, float z) {
		
		this.x = x;
		this.y = y;
		this.z = z;
		
		
		
	}
	
	public Vector3() {
		
		this.x = 0.0F;
		this.y = 0.0F;
		this.z = 0.0F;
		
		
		
	}
	
	
	
	
	
	

}
